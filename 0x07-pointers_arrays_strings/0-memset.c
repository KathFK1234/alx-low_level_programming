/**
 * _memset - fills memory with constant byte
 * @s: memory area string
 * @b: constant byte
 * @n: first set of bytes
 * Return: Pointer to memory area s
 */

char *memset(char *s, char b, unsigned int n)
{
	for (n = 0; *(s + c) != '\0'; n++)
	{
		s[c] = s[b];
	}
	return (s);
}
